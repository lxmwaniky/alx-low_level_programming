#include "3-calc.h"

/**
 * op_add - adds 2 integers
 * @a: first integer
 * @b: second integer
 * Return: answer
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subracts 2 integers
 * @a: first integer
 * @b: second integer
 * Return: answer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - adds 2 integers
 * @a: first integer
 * @b: second integer
 * Return: answer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - adds 2 integers
 * @a: first integer
 * @b: second integer
 * Return: answer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - adds 2 integers
 * @a: first integer
 * @b: second integer
 * Return: answer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
